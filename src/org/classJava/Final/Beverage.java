package org.classJava.Final;

public class Beverage {
	
	String name;
	double price;
	Boolean with_milk;
	int size;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public Boolean getWith_milk() {
		return with_milk;
	}
	public void setWith_milk(Boolean with_milk) {
		this.with_milk = with_milk;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
